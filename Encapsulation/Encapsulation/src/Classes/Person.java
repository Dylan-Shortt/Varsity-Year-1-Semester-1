package Classes;

// creates the class person
public class Person {
    //*************************************************************************************//
    private String name; // private = restricted access
    //*************************************************************************************//

    //*************************************************************************************//
    // Getter method
    public String getName() {
        // returns the object's name
        return name;
    }
    //*************************************************************************************//
    
    //*************************************************************************************//
    // Setter method receives a value
    public void setName(String newName) {
        // value overrides name
        this.name = newName;
    }
    //*************************************************************************************//
}
//*********************************END OF FILE***********************************//
