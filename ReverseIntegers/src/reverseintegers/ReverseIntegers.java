package reverseintegers;

import javax.swing.JOptionPane;

/**
 *
 * @author dylan
 */
public class ReverseIntegers {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // TODO code application logic here
        
        //**************************************************************************************//
        //recive input from the user
        String input = JOptionPane.showInputDialog(null, "Enter int values");
        int length = input.length() - 1;
        //**************************************************************************************//
        
        //**************************************************************************************//
        //output the values
        System.out.println("Entered : " + input);
        
        //output value index
        System.out.println("Index   : 0" + returnIndex(length));
        
        //output reversed values
        System.out.println("Return  : " + reverseInteger(input, length));
        //**************************************************************************************//
        
    }
    
    //**************************************************************************************//
    public static int returnIndex(int length)
    {
        //**************************************************************************************//
        //declerations
        int Index = 0;
        String temp = "";
        //**************************************************************************************//
        
        //**************************************************************************************//
        for(int i = 0; i <= length; i++)
        {
            temp += i + "";
        }
        //**************************************************************************************//
        
        //**************************************************************************************//
        //parse values to int
        Index = Integer.parseInt(temp);
        //**************************************************************************************//
        
        //**************************************************************************************//
        //return value
        return Index;
        //**************************************************************************************//
    }
    //**************************************************************************************//
    
    //**************************************************************************************//
    public static int reverseInteger(String value, int length)
    {
        //**************************************************************************************//
        //declare
        int returnValues = 0;
        String temp = "";
        //**************************************************************************************//
        
        //**************************************************************************************//
        //return the values of the string entered backwards
        for (int i = length; i >= 0; i--)
        {
            //assign values
            temp += value.charAt(i);
        }
        //**************************************************************************************//
        
        //**************************************************************************************//
        //assign new values 
        returnValues = Integer.parseInt(temp);
        //**************************************************************************************//
        
        //**************************************************************************************//
        //return new values
        return returnValues;
        //**************************************************************************************//
    }
    //**************************************************************************************//
    
}
